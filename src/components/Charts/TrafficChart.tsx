'use client'

import { Area, AreaChart, CartesianGrid, XAxis } from 'recharts'
import {
	Card,
	CardAction,
	CardContent,
	CardDescription,
	CardHeader,
	CardTitle,
} from '@/components/ui/card'
import {
	Select,
	SelectContent,
	SelectItem,
	SelectTrigger,
	SelectValue,
} from '@/components/ui/select'
import { getSiteTraffic, Traffic } from '@/lib/supabase/api'
import {
	ChartConfig,
	ChartContainer,
	ChartTooltip,
	ChartTooltipContent,
} from '@/components/ui/chart'
import { ToggleGroup, ToggleGroupItem } from '@/components/ui/toggle-group'
import { useEffect, useState } from 'react'
import { Skeleton } from '@/components/ui/skeleton'

const chartConfig = {
	visitors: {
		label: 'Пользователи',
	},
	desktop: {
		label: 'ПК',
		color: 'var(--chart-1)',
	},
	mobile: {
		label: 'Телефон',
		color: 'var(--chart-2)',
	},
} satisfies ChartConfig

export function TrafficChart() {
	const [timeRange, setTimeRange] = useState('7d')
	const [chartData, setChartData] = useState<Traffic[]>([])
	const [isLoading, setIsLoading] = useState(true)

	useEffect(() => {
		const getData = async () => {
			setIsLoading(true)
			try {
				const data = await getSiteTraffic()
				setChartData(data)
			} finally {
				setIsLoading(false)
			}
		}
		getData()
	}, [])

	const filteredData = chartData.filter(item => {
		const date = new Date(item.date)
		const referenceDate = new Date('2024-06-30')
		let daysToSubtract = 90
		if (timeRange === '30d') {
			daysToSubtract = 30
		} else if (timeRange === '7d') {
			daysToSubtract = 7
		}
		const startDate = new Date(referenceDate)
		startDate.setDate(startDate.getDate() - daysToSubtract)
		return date >= startDate
	})

	return (
		<Card className='@container/card'>
			<CardHeader>
				<CardTitle>Общие посещения</CardTitle>
				<CardDescription>
					<span className='hidden @[540px]/card:block'>Данные за последние 3 месяца</span>
					<span className='@[540px]/card:hidden'>Последние 3 месяца</span>
				</CardDescription>
				<CardAction>
					<ToggleGroup
						type='single'
						value={timeRange}
						onValueChange={setTimeRange}
						variant='outline'
						className='hidden *:data-[slot=toggle-group-item]:!px-4 @[767px]/card:flex'
					>
						<ToggleGroupItem value='7d'>Последние 7 дней</ToggleGroupItem>
						<ToggleGroupItem value='30d'>Последние 30 дней</ToggleGroupItem>
						<ToggleGroupItem value='90d'>Последние 3 месяца</ToggleGroupItem>
					</ToggleGroup>
					<Select value={timeRange} onValueChange={setTimeRange}>
						<SelectTrigger
							className='flex w-40 **:data-[slot=select-value]:block **:data-[slot=select-value]:truncate @[767px]/card:hidden'
							size='sm'
							aria-label='Select a value'
						>
							<SelectValue placeholder='Last 3 months' />
						</SelectTrigger>
						<SelectContent className='rounded-xl'>
							<SelectItem value='7d' className='rounded-lg'>
								Последние 7 дней
							</SelectItem>
							<SelectItem value='30d' className='rounded-lg'>
								Последние 30 дней
							</SelectItem>
							<SelectItem value='90d' className='rounded-lg'>
								Последние 3 месяца
							</SelectItem>
						</SelectContent>
					</Select>
				</CardAction>
			</CardHeader>
			<CardContent className='px-2 pt-4 sm:px-6 sm:pt-6'>
				{isLoading ? (
					<Skeleton className='h-100' />
				) : (
					<ChartContainer config={chartConfig} className='aspect-auto h-[250px] w-full'>
						<AreaChart data={filteredData}>
							<defs>
								<linearGradient id='fillDesktop' x1='0' y1='0' x2='0' y2='1'>
									<stop offset='5%' stopColor='var(--color-desktop)' stopOpacity={1.0} />
									<stop offset='95%' stopColor='var(--color-desktop)' stopOpacity={0.1} />
								</linearGradient>
								<linearGradient id='fillMobile' x1='0' y1='0' x2='0' y2='1'>
									<stop offset='5%' stopColor='var(--color-mobile)' stopOpacity={0.8} />
									<stop offset='95%' stopColor='var(--color-mobile)' stopOpacity={0.1} />
								</linearGradient>
							</defs>
							<CartesianGrid vertical={false} />
							<XAxis
								dataKey='date'
								tickLine={false}
								axisLine={false}
								tickMargin={8}
								minTickGap={32}
								tickFormatter={value => {
									const date = new Date(value)
									return date.toLocaleDateString('en-US', {
										month: 'short',
										day: 'numeric',
									})
								}}
							/>
							<ChartTooltip
								cursor={false}
								content={
									<ChartTooltipContent
										labelFormatter={value => {
											return new Date(value).toLocaleDateString('en-US', {
												month: 'short',
												day: 'numeric',
											})
										}}
										indicator='dot'
									/>
								}
							/>
							<Area
								dataKey='mobile'
								type='natural'
								fill='url(#fillMobile)'
								stroke='var(--color-mobile)'
								stackId='a'
							/>
							<Area
								dataKey='desktop'
								type='natural'
								fill='url(#fillDesktop)'
								stroke='var(--color-desktop)'
								stackId='a'
							/>
						</AreaChart>
					</ChartContainer>
				)}
			</CardContent>
		</Card>
	)
}
